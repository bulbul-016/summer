a = "my lovely \"Home\" is here."
print(a)
#my lovely "Home" is here.

#  \'	Single Quote	
c="it\'s so beautiful"
print(c)           
#it's so beautiful

#\\	Backslash
b= "have you ever been \\ here"
print(b)
#have you ever been \ here
	
#\n	New Line	
d="hello hello\nhow are you"
print(d)
#hello hello
#how are you

#\r	Carriage Return
e="are you normal\rgirl"
print(e)
#girlyou normal

#\t	Tab
txt = "Hello\tWorld!"
print(txt) 
#Hello   World!

#\b	Backspace
txt = "Hello     \bWorld!"
print(txt) 	
#Hello    World!

#\f	Form Feed
# \ooo	Octal value		
txt = "\110\145\154\154\157"
print(txt)
#Hello

#\xhh	Hex value
txt = "\x48\x65\x6c\x6c\x6f"
print(txt) 
#Hello